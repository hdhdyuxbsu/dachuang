// Helper function to get bitmap for a character code
static inline const uint16_t* get_char_bitmap(uint32_t code_point) {
    for (int i = 0; char_bitmaps[i].code_point != 0; i++) {
        if (char_bitmaps[i].code_point == code_point) {
            return char_bitmaps[i].bitmap;
        }
    }
    return NULL;
}

